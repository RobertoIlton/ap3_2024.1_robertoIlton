package br.com.roberto.aula2;

public class TiposPrimitivos {

    public static void main(String[] args) {
        byte codigoByte = 112;
        System.out.println("");
        System.out.println(codigoByte);

        short codigoShort = 114;
        System.out.println("");
        System.out.println(codigoShort);

        int codigoInt = 10;
        System.out.println("");
        System.out.println(codigoInt);

        long codigoLong = 213;
        System.out.println("");
        System.out.println(codigoLong);

        float codigoFloat = 111;
        System.out.println("");
        System.out.println(codigoFloat);

        double codigoDouble = 2;
        System.out.println("");
        System.out.println(codigoDouble);

        boolean codigoBoolean = true;
        System.out.println();
        System.out.println(codigoBoolean);

        char codigoChar = 'R';
        System.out.println("");
        System.out.println(codigoChar);

        String codigoString = "roberto";
        System.out.println("");
        System.out.println(codigoString);
    }
}
